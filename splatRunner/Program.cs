            {
                Console.Clear();
                Console.SetCursorPosition(cursorY, cursorX);
                Console.Write(playerSymbol);

                var move = Console.ReadKey();

                if (KeyActions.ContainsKey(move.Key))
                {
                    KeyActions[move.Key].Invoke();
                }
            }
        }

